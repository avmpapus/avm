	<p><h2><div style="padding:17px;">Запад</div></h2></p>
	<hr width="100%">
    Запад — сторона света, противоположная востоку. Первоначально это слово обозначало «закат», так как Солнце на закате всегда находится приблизительно на западе, кроме приполярных и полярных широт.

Русское обозначение запада — З, международное — W (от west).

Для того, чтобы идти на запад, используя для навигации компас, необходимо установить азимут 270 °. В дни равноденствия Солнце заходит на западе. Земля вращается с запада на восток. На географических картах западная сторона, как правило, находится слева.
